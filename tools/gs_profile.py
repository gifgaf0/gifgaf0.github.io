"""Gram-Schmidt profile of the Module-SLWE primal-attack lattice (Brief 05, OP-F).

Question (paraphrased from OP-F): does the Singer-cycle PSL(2,7)
structure imposed on the public matrix ``A`` of Module-SLWE
introduce *exploitable* short vectors in the primal-attack lattice
relative to a uniformly random Module-LWE matrix of the same
dimensions? Concretely: does the Gram-Schmidt profile of the
primal-lattice basis derived from a Singer-orbit ``A`` deviate from
the profile of a uniform-``A`` baseline, and if so by how much?

Setup
-----

- Module rank ``k = 32`` (per Brief 05 §1; reducible to 16 if
  resources are tight, see stop conditions).
- Sedenion dimension ``d = 16``.
- Primary prime ``q = p = 8191`` (smallest mod-455 prime above the
  toy size; preserves PSL(2,7) symmetry of the Singer construction).
- Flatten ``A`` (a ``k × k`` matrix of sedenion entries) into the
  left-multiplication embedding ``A_F`` of shape ``n × n`` with
  ``n = k · d``. Each sedenion entry ``A[i, j]`` becomes the
  ``d × d`` matrix of left-multiplication-by-``A[i, j]`` acting on
  ``S_p ≅ F_p^{16}``. This matches the existing ``rank_flat`` helper
  in ``tools/sqt_slwe.py``.
- Build an ``(n+1) × (n+1)`` primal-attack basis ``B`` whose first
  ``n`` rows are the rows of ``A_F`` and whose last row is the
  Kannan-style ``q``-tag ``(q, 0, …, 0, 1)``. The lattice
  generated by these rows is

      L = span_Z{ A_F-rows, (q, 0, …, 0, 1) }.

  This is a deliberately simple primal embedding chosen to expose
  any structural anomaly in ``A_F`` itself: the first ``n``
  ``b*_i`` come directly from Gram-Schmidting the rows of ``A_F``,
  so the GS norms of those rows are *the* observable for OP-F.

GS computation
--------------

Two backends:

- ``fpylll`` ``GSO.Mat(..., float_type='mpfr')`` for high-precision
  Gram-Schmidt over the lifted integer basis. Preferred at this
  scale.
- ``numpy.float64`` Gram-Schmidt as a fallback when fpylll isn't
  importable. Notes precision concerns inline.

The script reports the log_2 GS norms, the slope of the log
profile, the deviation from the Geometric Series Assumption (GSA)
prediction at every position, and the maximum log_2-deviation
between the Singer profile and the random baseline at any index.
A max deviation > 0.5 (T1 flag set by the brief) indicates a
potential structural weakness.
"""

